unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	rsl;

	i = 0;
	rsl = 0;
	while (src[rsl] != '\0')
		rsl++;
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (rsl);
}
