int		ft_strcmp(char *s1, char *s2)
{
	int		i;
	int		rsl;

	rsl = 0;
	i = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[i] || s2[i])
	{
		if (s1[i] < s2[i])
			return (rsl = (s1[i] - s2[i]));
		else if (s1[i] > s2[i])
			return (rsl = (s1[i] - s2[i]));
		i++;
	}
	return (rsl);
}
