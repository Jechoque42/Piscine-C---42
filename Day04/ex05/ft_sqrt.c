int	ft_sqrt(int nb)
{
	int	nb2;
	int carre;

	nb2 = 1;
	carre = 1;
	if (nb < 0)
		return (0);
	while (carre <= nb)
	{
		carre = nb2 * nb2;
		if (carre == nb)
			return (nb2);
		nb2++;
	}
	return (0);
}
