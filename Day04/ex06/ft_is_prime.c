int	ft_is_prime(int nb)
{
	int div;
	int rslt;

	div = nb - 1;
	rslt = 1;
	if (nb == 1 || nb == 0 || nb < 0)
		return (0);
	while (div != 1)
	{
		if (nb % div == 0)
			rslt = 0;
		div--;
	}
	return (rslt);
}
