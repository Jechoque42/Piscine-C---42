#include <unistd.h>

unsigned char	reverse_bits(unsigned char octet)
{
	unsigned char	res;
	int				count;

	res = 0;
	count = 8;
	while (count)
	{
		res = res * 2 + (unsigned char)(octet % 2);
		octet = octet / 2;
		count--;
	}
	return (res);
}

int		main(void)
{
	unsigned char c;

	c = '.';
	c = reverse_bits(c);
	write(1, &c, 1);
	return(0);
}
