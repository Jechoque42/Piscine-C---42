void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_words_tables(char **tab)
{
	int	k;

	k = 0;
	while (tab[k] != 0)
	{
		ft_putstr(tab[k]);
		ft_putchar('\n');
		k++;
	}
}
