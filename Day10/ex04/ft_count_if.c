int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int	rsl;

	i = 0;
	rsl = 0;
	while (tab[i] != 0)
	{
		if ((*f)(tab[i]) == 1)
			rsl++;
		i++;
	}
	return (rsl);
}
